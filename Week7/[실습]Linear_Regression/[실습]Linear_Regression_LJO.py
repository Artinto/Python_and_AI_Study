import numpy as np
import pandas as pd
from torch import nn
import torch
from torch import tensor

dataset_path="/content/drive/MyDrive/data-01-test-score.csv"
dataset=np.loadtxt(dataset_path, delimiter=',',dtype=np.float32)
x_data=tensor(dataset[:,:3])
y_data=tensor(dataset[:,3])

class Model(nn.Module):
    def __init__(self):
        super(Model, self).__init__()
        self.linear = torch.nn.Linear(3, 1)
    def forward(self, x):
        y_pred = self.linear(x)
        return y_pred

model = Model()
criterion = torch.nn.MSELoss(reduction='mean')
optimizer = torch.optim.SGD(model.parameters(), lr=0.00001)

for epoch in range(1000):
    y_pred = model(x_data)
    loss = criterion(y_pred, y_data)
    print(f'Epoch: {epoch} | Loss: {loss.item()} ')
    optimizer.zero_grad()
    loss.backward()
    optimizer.step()

hour_var = tensor(x_data[0])
y_pred = model(hour_var)
print("Prediction (after training)", x_data[0], ":", y_pred.item())

