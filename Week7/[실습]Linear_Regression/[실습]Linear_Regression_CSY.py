# 데이터 분석 라이브러리인 pandas 모듈 불러오기
# torch 모듈 내에서 neural network(신경망)의 약자인 nn을 불러옴. 이는 신경망을 구축하기 위한 데이터 구조나 레이어등이 정의되어 있음
# tensor 등의 다양한 수학 함수가 포함되어져 있는 torch 패키지를 가져옴
# torch에서 데이터의 배열인 tensor를 불러옴
# 클래스에서 train_test_split 함수를 가져와서 x_train, x_test, y_train_y_test 데이터셋을 분할함
import pandas as pd
from torch import nn
import torch
from torch import tensor
from sklearn.model_selection import train_test_split

# read_csv는 csv 파일을 읽어온 후 pandas의 기본 데이터 구조인 DataFrame으로 만들어줌. 인덱스를 자동으로 만들고 이름을 다음과 같이 줌
# 데이터 셋을 분할하는 과정으로 데이터 값들 중 x_train은 Q1,Q2,Q3 텐서 데이터로, y_train은 Final 텐서 데이터로, x_test와 y_test는 데이터의 4%인 한 줄을 차지하고 체계적으로 추출 하도록 함
df = pd.read_csv("/content/drive/data-01-test-score.csv",header = None, names = ['Q1','Q2','Q3','Final'])
x_train, x_test, y_train, y_test = train_test_split(torch.Tensor(df[['Q1','Q2','Q3']].values),torch.Tensor(df[['Final']].values),test_size=0.04,shuffle=False)

# torch.nn.Module을 상속받는 파이썬 클래스 정의
# 모델의 생성자 정의 (객체가 갖는 속성값을 초기화하는 역할,객체가 생성될 때 자동으로 호출됨)
class Model(nn.Module):
    def __init__(self):
        # super() 함수를 통해 nn.Module의 생성자를 호출함
        # nn.Linear() 함수를 이용하여 선형 모델을 만듦(3차원의 입력과 1차원의 출력을 인수로 받음)
        super(Model, self).__init__()
        self.linear = torch.nn.Linear(3, 1)

    # 모델 객체와 학습 데이터인 x를 받아 forward 연산하는 함수로 model(입력 데이터) 형식으로 객체를 호출하면 자동으로 forward 연산 수행됨
    def forward(self, x):
        # x를 인자로 넘겨 받아 선형 모델을 통해 계산된 값을 y_pred에 저장
        # y_pred 값 반환
        y_pred = self.linear(x)
        return y_pred

# Model 클래스 변수 model 선언
model = Model()

# 손실함수 정의 코드로, 손실 함수로 MSE(Mean Squared Error)를 사용함. reduction은 가공 데이터의 각 batch(데이터 샘플들의 묶음)의 값을 집계하는 방법으로 output의 평균을 냄
# SGD(확률적 경사 하강법)는 경사 하강법의 일종이고 model.parameters()를 이용하여 W와 b를 전달함. lr은 학습률이며 이는 경사하강법을 설정하는 코드
criterion = torch.nn.MSELoss(reduction='mean')
optimizer = torch.optim.SGD(model.parameters(), lr=0.00005)

# 학습을 1000번 반복
for epoch in range(1000):
    # x_train를 선형 모델에 인자로 전달하여 y_pred 값을 계산함
    y_pred = model(x_train)

    # y_pred와 y_train를 인자로 넘겨 받아 손실함수를 계산하여 저장
    # 학습을 반복한 횟수와 손실함수 값을 출력
    loss = criterion(y_pred, y_train)
    print(f'Epoch: {epoch} | Loss: {loss.item()} ')

    # 미분을 통해 얻은 기울기가 이전에 계산된 기울기 값에 누적되기 때문에 기울기 값을 0으로 초기화하여 새로운 (가중치와 편향)에 대한 새로운 기울기를 구할 수 있도록 함
    # 역전파 실행, 손실 함수를 미분하여 기울기 계산
    # step() 함수를 호출하여 W와 b를 업데이트함
    optimizer.zero_grad()
    loss.backward()
    optimizer.step()

# 학습을 끝낸 후 x_test값을 입력으로 모델의 예측값을 y_pred에 대입
# 훈련 후 입력이 96,93,95일 때 예측된 y값 출력 (192에 가까워야 함)
y_pred = model(x_test)
print("Prediction (after training)", 96, 93, 95, y_pred.item())

