import numpy as np

Data = np.loadtxt('diabetes.csv', delimiter=',')

x_data = Data[:,0:-1]
y_data = Data[:,[-1]]
w = np.random.rand(8,1)
learning_rate = 0.1

def sigmoid(x):
    return 1/(1 + np.exp(-x))

def loss_f(x, y, w):
    delta = 1e-7
    z=np.dot(x,w)
    y_pred=sigmoid(z)
    return -(np.sum(y*np.log(y_pred+delta)+(1-y)*np.log((1-y_pred)+delta))) / len(x)

def predict(x, w):
    y_pred = sigmoid(np.dot(x,w))
    
    result = 0
    
    if y_pred > 0.5 :
        result = 1
    else:
        result = 0
        
    return result

def Numerical_derivative(w):
    delta = 10e-1
    gradient = np.zeros_like(w)  # weight array와 같은 크기를 가진 빈 값의 Gradient 생성
    
    for idx in range(w.size):
        tmp = w[idx]
        
        w[idx] = float(tmp) + delta
        fx1 = loss_f(x_data, y_data, w) 

        w[idx] = float(tmp) - delta
        fx2 = loss_f(x_data, y_data, w) 
        
        w[idx] = tmp
        
        gradient[idx] = (fx1 - fx2) / 2*delta
    return gradient

def accuracy(x, y,w):
    ACC = 0
    for x_,y_ in zip(x,y):
        if predict(x_, w) == y_:
            ACC += 1
    result = ( ACC/len(x) ) * 100
    return result
            
    
for epoch in range(1001):
    w -= learning_rate*Numerical_derivative(w)
    print("Epoch: ", epoch, "/1000" , "Error: ", loss_f(x_data,y_data, w))

print("Accuracy : ", accuracy(x_data,y_data,w), "%")

