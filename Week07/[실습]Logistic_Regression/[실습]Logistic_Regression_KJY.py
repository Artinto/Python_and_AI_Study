import pandas as pd
import torch
from torch import nn
from torch import sigmoid
import torch.optim as optim

csv_test=pd.read_csv('C:/input_file/diabetes/diabetes.csv', header=None, names=['A','B','C','D','E','F','G','H','I'])

x=csv_test[['A','B','C','D','E','F','G','H']]

y=csv_test['I']

x_data = torch.FloatTensor(x.values)

y_data = torch.FloatTensor(y.values)

y_data=y_data.unsqueeze(-1)

class Model(nn.Module):
    def __init__(self):
        """
        In the constructor we instantiate nn.Linear module
        """
        super(Model, self).__init__()
        self.linear = nn.Linear(8, 1)  # 8 in and 1 out

    def forward(self, x):
        """
        In the forward function we accept a Variable of input data and we must return
        a Variable of output data.
        """
        y_pred = sigmoid(self.linear(x))
        return y_pred
    
def accuracy(y_predic,y_real):
    n=0
    for i in range(y_predic.shape[0]):
        if y_predic[i].item()==y_real[i].item():
            n+=1
    return n
# our model
model = Model()

criterion=nn.BCELoss(reduction='mean')
optimizer=optim.SGD(model.parameters(), lr=0.5)

for epoch in range(1000):
    y_pred=model(x_data)
    loss=criterion(y_pred, y_data)
    n=accuracy(torch.round(y_pred),y_data)
    print(f'Epoch {epoch+1}/1000 | Loss: {loss.item():4f} | accuracy: {(n/y_data.shape[0])*100:4f}')
    
    optimizer.zero_grad()
    loss.backward()
    optimizer.step()
    
# After training
print(f'\nLet\'s predict the hours need to score above 50%\n{"=" * 50}')
hour_var = model(torch.Tensor([[-0.294118,0.487437,0.180328,-0.292929,0.000000,0.001490,-0.531170,-0.033333]]))
print(f'Prediction after 1 hour of training: {hour_var.item():.4f} | Above 50%: {hour_var.item() > 0.5}')
      

