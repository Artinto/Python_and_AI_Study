import numpy as np
import pandas as pd
from torch import nn
import torch
from torch import tensor

dataset_path="/data-01-test-score.csv"
dataset=np.loadtxt(dataset_path, delimiter=',',dtype=np.float32)
x_data=tensor(dataset[:,0:3])
y_data=tensor(dataset[:,3:4])
#print(x_data)
#print(y_data)
class Model(nn.Module): # nn.Module을 상속받는  Model class 생성
    def __init__(self): # initial
        super(Model, self).__init__()        # 부모 클래스 생성자 호출
        self.linear = torch.nn.Linear(3, 1)  # One in and one out

    def forward(self, x): # 선형 모델 계산
        y_pred = self.linear(x)
        return y_pred

model = Model()
criterion = torch.nn.MSELoss(reduction='sum')
optimizer = torch.optim.SGD(model.parameters(), lr=0.000001)

for epoch in range(1000): # == for(i = 0; i < 1000; i++)
    y_pred = model(x_data) # 예측값을 저장

    loss = criterion(y_pred, y_data)
    print(f'Epoch: {epoch} | Loss: {loss.item()} ')

    optimizer.zero_grad() # backword 하기 전 gradient를 초기화 (cuz, gradient가 덮여쓰여지지 않고 누적되기 때문)
    loss.backward()       # loss에 대한 backword 진행
    optimizer.step()      # parameter 초기화


# After training
hour_var = tensor([[100.0, 100.0, 100.0]])
y_pred = model(hour_var)
